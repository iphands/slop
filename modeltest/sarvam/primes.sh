# कार्यक्रम का उद्देश्य: 1 से 10,000 तक के सभी अभाज्य संख्याओं को खोजना
# Sieve of Eratosthenes विधि का उपयोग करके

# अधिकतम सीमा निर्धारित करना
SEEMA=10000

# एक सरणी बनाना जिसमें सभी संख्याएं शुरुआत में अभाज्य मानी गई हैं
# 0 = अभाज्य, 1 = अभाज्य नहीं
declare -a chalni
for (( i=0; i<=SEEMA; i++ )); do
    chalni[$i]=0
done

# 0 और 1 अभाज्य संख्याएं नहीं हैं
chalni[0]=1
chalni[1]=1

# Eratosthenes की चालनी — छोटी संख्याओं के गुणकों को हटाना
i=2
while (( i * i <= SEEMA )); do
    # यदि संख्या अभी भी अभाज्य है तो उसके गुणकों को हटाओ
    if [[ ${chalni[$i]} -eq 0 ]]; then
        # i के सभी गुणकों को गैर-अभाज्य चिह्नित करना
        j=$(( i * i ))
        while (( j <= SEEMA )); do
            chalni[$j]=1
            j=$(( j + i ))
        done
    fi
    (( i++ ))
done

# परिणाम छापना — सभी अभाज्य संख्याएं
echo "# 1 से ${SEEMA} तक की अभाज्य संख्याएं:"
echo "# ========================================"

# गणना करने के लिए काउंटर
ginti=0

for (( n=2; n<=SEEMA; n++ )); do
    if [[ ${chalni[$n]} -eq 0 ]]; then
        echo "$n"
        (( ginti++ ))
    fi
done

# अंत में कुल अभाज्य संख्याओं की गिनती बताना
echo ""
echo "# कुल अभाज्य संख्याएं मिली: ${ginti}"
