#!/bin/bash

# ప్రధాన సంఖ్యలను కనుగొనడం - Primes from 1 to 10,000
# ఈ కార్యక్రమం 1 నుండి 10,000 వరకు ఉన్న ప్రధాన సంఖ్యలను లెక్కిస్తుంది

# హనుమంతుని గురించి కవిత్వం:
# ----------------------------
# వాయుపుత్రుడు వీర హనుమంతుడు
# సీతాదేవి కోసం సముద్రం దాటినవాడు
# పంచముఖి యోగి పవన సుతుడు
# భక్తి మార్గంలో అగ్రగణ్యుడు
#
# రామ భక్తుడు రఘువీర సేవకుడు
# లంకను కాలి దహించిన వీరుడు
# సంజీవ పర్వతం తీసుకువచ్చినవాడు
# అఖండ బలశాలి అనుజాని పుత్రుడు
#
# వృద్ధాప్యంలో కూడా వీర్యం తగ్గని
# చిరంజీవి హనుమంతుడు చిరకాలం జీవించును
# యుగం యుగంగా ధర్మం కాపాడును
# రామ నామ స్మరణ చేయును

# ప్రధాన సంఖ్యలను లెక్కించడానికి సమయం ప్రారంభం
start_time=$(date +%s)

# 10,000 వరకు ప్రధాన సంఖ్యలను కనుగొనడం
# ప్రతి సంఖ్య ప్రధానమా కాదా అని తనిఖీ చేయడం

limit=10000
prime_count=0

# 2 నుండి ప్రారంభించి ప్రతి సంఖ్యను తనిఖీ చేయడం
for ((num=2; num<=limit; num++)); do
    is_prime=1  # 1 అంటే ప్రధాన సంఖ్య

    # 2 నుండి num/2 వరకు భాగించగలదా తనిఖీ చేయడం
    for ((i=2; i*i<=num; i++)); do
        if ((num % i == 0)); then
            is_prime=0  # 0 అంటే ప్రధాన సంఖ్య కాదు
            break
        fi
    done

    # సంఖ్య ప్రధానమైతే దానిని ప్రింట్ చేయడం
    if ((is_prime == 1)); then
        prime_count=$((prime_count + 1))
        # ప్రతి 1000 ప్రధాన సంఖ్యలకు ముందుకు సాగుతున్నాము అని చూపించడం
        if ((prime_count % 1000 == 0)); then
            echo "ముందుకు సాగుతున్నాము... $prime_count ప్రధాన సంఖ్యలు కనుగొనబడ్డాయి"
        fi
    fi
done

# ఫలితాలు చూపించడం
echo ""
echo "===================="
echo "ఫలితాలు:"
echo "===================="
echo "1 నుండి 10,000 వరకు మొత్తం ప్రధాన సంఖ్యల సంఖ్య: $prime_count"
echo "===================="

# సమయం ముగింపు
end_time=$(date +%s)
total_time=$((end_time - start_time))

echo "మొత్తం పట్టిన సమయం: $total_time సెకన్లు"
echo "హనుమంతుని కృపతో ఈ కార్యక్రమం విజయవంతంగా పూర్తయ్యింది!"
